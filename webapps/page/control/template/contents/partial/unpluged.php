			<p class="text-muted">
				RaspberryPi（ラズベリーパイ）は、イギリスのラズベリー財団にて開発された教育用の小型PCです。
				<br>
				GPIOピン（写真の黒いコードが繋がっている部分）を使って、LED・サーボモーターをプログラムで制御し、
				<br>
