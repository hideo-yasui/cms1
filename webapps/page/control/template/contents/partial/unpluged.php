				<br>
				最初はLEDを点けたり消したりするところから、
				<br>
				徐々にモーターやセンサーなどの複雑な部品の制御方法を覚えます。
				<br>
				<br>
				RaspberryPiもScratchを使ってプログラミングを行います。
			</p>
			<hr>
			<strong><i class="fa fa-book  mr-1"></i>進め方</strong>
			<p class="text-muted">
