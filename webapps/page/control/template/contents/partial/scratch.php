			<strong><i class="fa fa-question-circle  mr-1"></i>Scratch（スクラッチ）とは</strong>
			<img class="profile-user-img img-fluid float-right m-2" src="/img/school/scratch.png" alt="" />
			<p class="text-muted">
				Scratch（スクラッチ）は、マサチューセッツ工科大学（MIT）にて開発されたプログラミング学習環境です。
				<br>
				NHKのEテレでWHY プログラミングという番組でも扱っており、
				<br>
				画面に、パズルのようなブロックをつないで、プログラミングを作成していく、
				<br>
				プログラミング入門には最適な教材となっております。
			</p>
			<hr>
			<strong><i class="fa fa-chalkboard-teacher  mr-1"></i>学習内容</strong>
			<p class="text-muted">
				Scratchを使って、はじめはシンプルなゲームやアニメーションを作りながら、
				<br>
				使い方を学習し、プログラミングに登場する主要な要素を覚えていきます。
				<br>
				<br>
				慣れてきたら、複雑な内容に挑戦していき、理解を深めていきます。
			</p>
			<hr>
			<strong><i class="fa fa-book  mr-1"></i>進め方</strong>
			<p class="text-muted">
				製作する目標のゲームやアプリを決め、開発を進めます。
				<br>
				プログラミングをしていくと、「なぜこうなるのか」、「なぜこうするのか」といった疑問が生まれます。
				<br>
				わからない場合や、詰まった場合は、その場で、解説し解消していきます。
				<br>
				<br>
				自習の時間では、自分なりに操作して、理解を深めることができ、
				<br>
				計算や数式が登場した場合は、必要に応じて、算数を学習していくことになります。
				<br>
				作りたいゲームや機能があれば、一緒に考え、実現に向けサポートいたします。
			</p>
